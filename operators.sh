#!/bin/bash

# Arithmetic operators example

num1=10
num2=5

echo "num1 = $num1"
echo "num2 = $num2"

# Addition
result=$((num1 + num2))
echo "num1 + num2 = $result"

# Subtraction
result=$((num1 - num2))
echo "num1 - num2 = $result"

# Multiplication
result=$((num1 * num2))
echo "num1 * num2 = $result"

# Division
result=$((num1 / num2))
echo "num1 / num2 = $result"

# Modulo
result=$((num1 % num2))
echo "num1 % num2 = $result"

